public class Tienda {

    private String codigoTienda = "";
    private String nombreTienda = "";
    private String ubicacionTienda = "";
    private String estadoTienda = "";

    /**
     * @return the codigoTienda
     */
    public String getCodigoTienda() {
        return codigoTienda;
    }

    /**
     * @param codigoTienda the codigoTienda to set
     */
    public void setCodigoTienda(String codigoTienda) {
        this.codigoTienda = codigoTienda;
    }

    /**
     * @return the nombreTienda
     */
    public String getNombreTienda() {
        return nombreTienda;
    }

    /**
     * @param nombreTienda the nombreTienda to set
     */
    public void setNombreTienda(String nombreTienda) {
        this.nombreTienda = nombreTienda;
    }

    /**
     * @return the ubicacionTienda
     */
    public String getUbicacionTienda() {
        return ubicacionTienda;
    }

    /**
     * @param ubicacionTienda the ubicacionTienda to set
     */
    public void setUbicacionTienda(String ubicacionTienda) {
        this.ubicacionTienda = ubicacionTienda;
    }

    /**
     * @return the estadoTienda
     */
    public String getEstadoTienda() {
        return estadoTienda;
    }

    /**
     * @param estadoTienda the estadoTienda to set
     */
    public void setEstadoTienda(String estadoTienda) {
        this.estadoTienda = estadoTienda;
    }
    
    
}
