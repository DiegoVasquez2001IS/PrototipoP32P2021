    private void JBtnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBtnBuscarActionPerformed
        Cliente objCliente = new Cliente();
        ClienteDAO objClienteDAO = new ClienteDAO();
        objCliente.setNumeroTarjeta(JTxtNumeroTarjeta.getText());

        objCliente = objClienteDAO.query(objCliente);

        if (!objCliente.getNumeroTarjeta().equals("")) {

            JOptionPane.showMessageDialog(null, "¡Registro Buscado Exitosamente!", "Información", JOptionPane.INFORMATION_MESSAGE);
            JTxtNumeroTarjeta.setText(objCliente.getNumeroTarjeta());
            JTxtNombreCliente.setText(objCliente.getNombreCliente());
            JTxtDpiCliente.setText(objCliente.getDpiCliente());
            JTxtTelefonoCliente.setText(objCliente.getTelefonoCliente());
            JTxtDireccionCliente.setText(objCliente.getDireccionCliente());
            JTxtCorreoCliente.setText(objCliente.getCorreoCliente());
        } else {
            JOptionPane.showMessageDialog(null, "Registro Inexistente", "Error", JOptionPane.ERROR_MESSAGE);
        }
        try {
            llenar_tabla();
        } catch (SQLException ex) {
            Logger.getLogger(JFrmMantCliente.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_JBtnBuscarActionPerformed

