    public void llenar_tabla() throws SQLException {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Tarjeta");
        modelo.addColumn("Cliente");
        modelo.addColumn("DPI");
        modelo.addColumn("Teléfono");
        modelo.addColumn("Dirección");
        modelo.addColumn("Correo");
        
        ClienteDAO objClienteDAO = new ClienteDAO();
        List<Cliente> cliente = objClienteDAO.select();
        JTableCliente.setModel(modelo);
        String[] dato = new String[6];
        for (int i = 0; i < cliente.size(); i++) {
            dato[0] = cliente.get(i).getNumeroTarjeta();
            dato[1] = cliente.get(i).getNombreCliente();
            dato[2] = cliente.get(i).getDpiCliente();
            dato[3] = cliente.get(i).getTelefonoCliente();
            dato[4] = cliente.get(i).getDireccionCliente();
            dato[5] = cliente.get(i).getCorreoCliente();
            modelo.addRow(dato);
        }
    }
    private void JBtnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBtnAgregarActionPerformed
        ClienteDAO objClienteDAO = new ClienteDAO();
        Cliente objCliente = new Cliente();

        objCliente.setNumeroTarjeta(JTxtNumeroTarjeta.getText());
        objCliente.setNombreCliente(JTxtNombreCliente.getText());
        objCliente.setDpiCliente(JTxtDpiCliente.getText());
        objCliente.setTelefonoCliente(JTxtTelefonoCliente.getText());
        objCliente.setDireccionCliente(JTxtDireccionCliente.getText());
        objCliente.setCorreoCliente(JTxtCorreoCliente.getText());
        objCliente.setBonosAcumulados("0");
        objCliente.setRentaAcumulada("0");
        objCliente.setMoraAcumulada("0");

        if (objCliente != null) {
            if (objClienteDAO.insert(objCliente) > 0) {
                JOptionPane.showMessageDialog(null, "¡Registro Exitoso!", "Información", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Registro No Exitoso", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        
        try {
            llenar_tabla();
        } catch (SQLException ex) {
            Logger.getLogger(JFrmMantCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_JBtnAgregarActionPerformed

